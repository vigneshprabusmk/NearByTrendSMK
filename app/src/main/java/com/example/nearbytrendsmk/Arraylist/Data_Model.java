package com.example.nearbytrendsmk.Arraylist;

public class Data_Model {

	String name, content;

	// Setter for database
	public Data_Model(String name, String content) {
		this.name = name;
		this.content = content;

	}

	// Getter
	public String getName() {
		return name;
	}

	public String getcontent() {
		return content;
	}

}
